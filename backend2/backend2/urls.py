from django.contrib import admin
from django.urls import path, include
from dj_rest_auth.views import PasswordResetView, PasswordResetConfirmView
from allauth.account import urls as allauth_urls

from aiMathpad import views


urlpatterns = [
    path('', views.index, name='index'),
    path('admin/', admin.site.urls),
    path(('users/'), include('aiMathpad.user_urls')),
    path('password-reset/', PasswordResetView.as_view()),
    path('password-reset-confirm/<uidb64>/<token>/', PasswordResetConfirmView.as_view(), name='password_reset_confirm'),  
]

from django.conf import settings

if settings.REST_AUTH['USE_JWT']:
    from rest_framework_simplejwt.views import TokenVerifyView

    from dj_rest_auth.jwt_auth import get_refresh_view

    urlpatterns += [
        path('token/verify/', TokenVerifyView.as_view(), name='token_verify'),
        path('token/refresh/', get_refresh_view().as_view(), name='token_refresh'),
    ]

urlpatterns+=[
    path('procImg/',views.proc_image),
    path('saveAnnot/',views.store_annot),
]

urlpatterns+=[
    path('accounts/', include(allauth_urls)),
    path('dataAdmin/', include('dataAdmin.urls')),
]

if settings.DEBUG:
    from django.conf.urls.static import static
    urlpatterns += static(settings.MEDIA_URL, document_root=settings.MEDIA_ROOT)

